#  Write a Python program to get a string made of the first 2 and the
#  last 2 characters from strings, “world”, “Dongguk”, and “programming”.

a = "world"
b = "Dongguk"
c = "programming"

a_first = a[0:2]
print(a_first)
a_last = a[-2:]
print(a_last)

b_first = b[0:2]
print(b_first)
b_last = b[-2:]
print(b_last)

c_first = c[0:2]
print(c_first)
c_last = c[-2:]
print(c_last)